package oopl;

public class Increment {

	public static void main(String[] args) {
		int a,b,c,d;
		a=1;b=2;
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		c=++b;
		System.out.println("c = "+c);
		d=++a;
		System.out.println("d = "+d);
		c++;
		System.out.println("c = "+c);
		

	}

}
