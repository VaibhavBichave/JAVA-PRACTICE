package oopl;

public class SimpleOperation {

	public static void main(String[] args) {
		int x=10,y=20;double z=25.98;
		System.out.println("x=10,y=20,z=25.98");
		System.out.println("x+y = "+(x+y));
		System.out.println("z-y = "+(z-y));
		System.out.println("x*y = "+x*y);
		System.out.println("z/y = "+z/y);
		System.out.println("z%y = "+z%y);
		
		
	}

}
